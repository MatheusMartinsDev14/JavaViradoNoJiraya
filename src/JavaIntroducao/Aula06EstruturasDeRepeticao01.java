package JavaIntroducao;

public class Aula06EstruturasDeRepeticao01 {
    public static void main(String[] args) {
        int count = 0;

        while (count < 10) {
            System.out.println(count);
            count++;
        }

        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }
    }
}
